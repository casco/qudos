export const qft_qiskit_example=`
from qiskit import QuantumCircuit, transpile
from qiskit_aer import QasmSimulator
import math

QUBIT_COUNT = 3
QUBITS = range(QUBIT_COUNT)

def run_circuit(qc: QuantumCircuit):
    sim = QasmSimulator()
    transpiled = transpile(qc)
    result = sim.run(transpiled).result()
    return result

def analyze_qft_results(results):
    counts = results.get_counts()
    print("Resultados:", counts)
    total = sum(counts.values())
    percentages = {key: f"{value / total*100:.2f}%" for key, value in counts.items()}
    ordered_percentages = dict(sorted(percentages.items(), key=lambda item: item[1], reverse=True))
    print("Porcentajes:", ordered_percentages)


def qft(qc):
    """
    Aplica la Transformada de Fourier Cuántica al circuito
    """
    # swap
    qc.barrier()
    for i in range(QUBIT_COUNT//2):
        qc.swap(i, QUBIT_COUNT-i-1)
    qc.barrier()

    for i in range(QUBIT_COUNT-1):
        qc.h(i)
        qc.barrier()
        for j in range(i+1):
            qc.cp(math.pi/(2**(j+1)), i-j, i+1)

    qc.h(QUBIT_COUNT-1)

def inverse_qft(qc):
    """
    Aplica la Transformada de Fourier Cuántica inversa al circuito
    """
    for i in range(QUBIT_COUNT-1):
        qc.h(QUBIT_COUNT-1-i)
        qc.barrier()
        for j in range(QUBIT_COUNT-i-1):
            qc.cp(-math.pi/(2**(j+1)), QUBIT_COUNT-2-i-j, QUBIT_COUNT-1-i)
        qc.barrier()

    qc.h(0)
    # swap
    qc.barrier()
    for i in range(QUBIT_COUNT//2):
        qc.swap(i, QUBIT_COUNT-i-1)

qc = QuantumCircuit(QUBIT_COUNT, QUBIT_COUNT)

qc.x(2) # frecuencia de la onda
qc.barrier()
inverse_qft(qc) # generar la onda
qc.barrier()
qft(qc) # obtener la frecuencia
qc.barrier()

qc.measure(QUBITS, QUBITS)
print(qc.draw())

res = run_circuit(qc)
analyze_qft_results(res)
`

